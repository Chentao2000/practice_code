public class Jvxing
{
    //成员变量
    private double width;
    private double chang;
    public double getWidth() {
        return width;
    }
    public void setWidth(double width) {
        this.width = width;
    }
    public double getChang() {
        return chang;
    }
    public void setChang(double height) {
        this.chang = height;
    }
    
    
    //成员方法
    public double mianji()
    {
        return this.width*this.chang;
    }
}
